// Store State Interface
interface TableStylesState {
  styles: TableStyleConfig[];
  activeStyleId: string;
  customStyles: TableStyleConfig[];

  // Actions
  setActiveStyle: (styleId: string) => void;
  getActiveStyle: () => TableStyleConfig | undefined;
  addCustomStyle: (style: Omit<TableStyleConfig, 'id' | 'isCustom'>) => string;
  updateCustomStyle: (id: string, updates: Partial<TableStyleConfig>) => void;
  deleteCustomStyle: (id: string) => void;
  duplicateStyle: (id: string) => string;
  getAllStyles: () => TableStyleConfig[];
  getStylesByCategory: (category: TableStyleCategory) => TableStyleConfig[];
}

export const useTableStylesStore = create<TableStylesState>()(
  persist(
    (set, get) => ({
      styles: defaultTableStyles,
      activeStyleId: 'theme-default',
      customStyles: [],

      setActiveStyle: (styleId) => {
        set({ activeStyleId: styleId });
      },

      getActiveStyle: () => {
        const state = get();
        return state.styles.find(s => s.id === state.activeStyleId)
          || state.customStyles.find(s => s.id === state.activeStyleId)
          || state.styles[0];
      },

      addCustomStyle: (style) => {
        const id = `custom-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
        const newStyle: TableStyleConfig = {
          ...style,
          id,
          isCustom: true,
          category: 'custom',
        };
        set((state) => ({
          customStyles: [...state.customStyles, newStyle],
        }));
        return id;
      },

      updateCustomStyle: (id, updates) => {
        set((state) => ({
          customStyles: state.customStyles.map((s) =>
            s.id === id ? { ...s, ...updates } : s
          ),
        }));
      },

      deleteCustomStyle: (id) => {
        set((state) => ({
          customStyles: state.customStyles.filter((s) => s.id !== id),
          activeStyleId: state.activeStyleId === id ? 'theme-default' : state.activeStyleId,
        }));
      },

      duplicateStyle: (id) => {
        const state = get();
        const original = state.styles.find(s => s.id === id) || state.customStyles.find(s => s.id === id);
        if (!original) return '';

        const newId = `custom-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
        const duplicated: TableStyleConfig = {
          ...original,
          id: newId,
          name: `${original.name} (Copy)`,
          isCustom: true,
          category: 'custom',
        };
        set((state) => ({
          customStyles: [...state.customStyles, duplicated],
        }));
        return newId;
      },

      getAllStyles: () => {
        const state = get();
        return [...state.styles, ...state.customStyles];
      },

      getStylesByCategory: (category) => {
        const state = get();
        const allStyles = [...state.styles, ...state.customStyles];
        return allStyles.filter(s => s.category === category);
      },
    }),
    {
      name: 'rustpress-table-styles',
    }
  )
);

