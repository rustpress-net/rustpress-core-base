// Table Style Definition
export interface TableStyleConfig {
  // Basic Info
  id: string;
  name: string;
  category: TableStyleCategory;
  description: string;
  isCustom: boolean;

  // Header Styles
  header: {
    backgroundColor: string;
    textColor: string;
    fontWeight: 'normal' | 'medium' | 'semibold' | 'bold';
    fontSize: 'xs' | 'sm' | 'base' | 'lg';
    textTransform: 'none' | 'uppercase' | 'lowercase' | 'capitalize';
    borderBottom: string;
    padding: string;
  };

  // Body Styles
  body: {
    backgroundColor: string;
    alternateRowColor: string;
    textColor: string;
    fontSize: 'xs' | 'sm' | 'base' | 'lg';
    padding: string;
  };

  // Border Styles
  border: {
    style: 'none' | 'solid' | 'dashed' | 'dotted';
    width: string;
    color: string;
    radius: string;
    cellBorders: boolean;
    headerBorder: boolean;
  };

  // Effects
  effects: {
    striped: boolean;
    hoverable: boolean;
    hoverColor: string;
    shadow: 'none' | 'sm' | 'md' | 'lg' | 'xl';
    animation: 'none' | 'fade' | 'slide' | 'scale';
  };

  // Footer Styles (optional)
  footer?: {
    backgroundColor: string;
    textColor: string;
    fontWeight: 'normal' | 'medium' | 'semibold' | 'bold';
    borderTop: string;
  };
}

